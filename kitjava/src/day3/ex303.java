public class ex303 {
	public static void main(String[] args) {
		char c = 'A';
		System.out.println("c : " + c); //c = A
		
		char d = 65;
		System.out.println("d : " + d); //c = A --> 아스키코드에 의해서 65 = A
		
		char e = 'Z';
		System.out.println("e : " + e);
		
		char z = 90;
		System.out.println("z : " + z);
	}
}
